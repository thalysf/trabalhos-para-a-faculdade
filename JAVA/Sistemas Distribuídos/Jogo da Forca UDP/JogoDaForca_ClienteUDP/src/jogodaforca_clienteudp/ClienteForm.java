    private void btnNovoTermoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoTermoActionPerformed
        if (meuCliente.getChances() == 0 || !meuCliente.getMascara().contains("_")) {
            solicitarNovaPalavra();
            exibirPontuacao();
            exibirLetrasTentadas();
        } else {
            JOptionPane.showMessageDialog(this, "Antes de iniciar um novo jogo, finalize o que está em andamento!");
        }
    }//GEN-LAST:event_btnNovoTermoActionPerformed

