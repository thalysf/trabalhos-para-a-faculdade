    private void btnMeuHistoricoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMeuHistoricoActionPerformed
        String acertos = "";
        String erros = "";
        meuCliente.solicitarHistoricoJogador();
        
        for(String palavra: meuCliente.getPalavrasAcertadas())
        {
            acertos += palavra + ", ";
        }
        
        for(String palavra: meuCliente.getPalavrasErradas())
        {
            erros += palavra + ", ";
        }
        JOptionPane.showMessageDialog(this, "Seu histórico: \n Palavras acertadas: " + acertos + "\n Palavras erradas: " + erros);
        
    }//GEN-LAST:event_btnMeuHistoricoActionPerformed

