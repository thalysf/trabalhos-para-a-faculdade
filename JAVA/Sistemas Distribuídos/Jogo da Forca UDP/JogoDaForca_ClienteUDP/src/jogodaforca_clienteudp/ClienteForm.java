    private void btnEnviarLetraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarLetraActionPerformed
        if (meuCliente.getJogador() != null) {
            String letra = letraChute.getText();
            if (letra.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Infelizmente meu amigo, você não sabe jogar, vai ter que aprender. Insira uma letra que você deseja jogar.");
            } else {
                meuCliente.chutarLetra(letra.charAt(0));
                txtPalavra.setText(meuCliente.getMascara());
                exibirPontuacao();
                exibirLetrasTentadas();
            }
            if (meuCliente.getChances() == 0) {
                JOptionPane.showMessageDialog(this, "Infelizmente não foi dessa vez, você perdeu! =(");
                txtPalavra.setText(meuCliente.getPalavra());
                txtResultado.setText("Derrota!");
                meuCliente.atualizaPlacar("DERROTA");
            }
            if (!meuCliente.getMascara().contains("_")) {
                JOptionPane.showMessageDialog(this, "Parabéns " + meuCliente.getJogador() + "! você ganhou! =)");
                txtResultado.setText("Vitória!");
                meuCliente.atualizaPlacar("VITORIA");
            }
            letraChute.setText("");
        }
    }//GEN-LAST:event_btnEnviarLetraActionPerformed

